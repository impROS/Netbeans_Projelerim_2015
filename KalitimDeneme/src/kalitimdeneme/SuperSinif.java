
package kalitimdeneme;
public class SuperSinif {

    public SuperSinif() {
    }
    
    public int topla(int sayi1,int sayi2){
    return sayi1+sayi2;
    }
    
    protected int cikar(int sayi1,int sayi2){
    return sayi1-sayi2;
    }
    
    public final int bol(int sayi1,int sayi2){
    return sayi1/sayi2;
    }
    
    private int carp(int sayi1,int sayi2){
    return sayi1 * sayi2;
    }
}
