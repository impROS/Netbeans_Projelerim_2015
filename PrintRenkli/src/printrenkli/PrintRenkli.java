package printrenkli;

public class PrintRenkli {
public static final String ANSI_RESET = "\u001B[0m";
public static final String ANSI_BLACK = "\u001B[30m";
public static final String ANSI_RED = "\u001B[31m";
public static final String ANSI_GREEN = "\u001B[32m";
public static final String ANSI_YELLOW = "\u001B[33m";
public static final String ANSI_BLUE = "\u001B[34m";
public static final String ANSI_PURPLE = "\u001B[35m";
public static final String ANSI_CYAN = "\u001B[36m";
public static final String ANSI_WHITE = "\u001B[37m";
    public static void main(String[] args) {
System.out.println(ANSI_BLACK + "impROS" + ANSI_RESET);
System.out.println(ANSI_RED + "impROS" + ANSI_RESET);
System.out.println(ANSI_GREEN + "impROS" + ANSI_RESET);
System.out.println(ANSI_YELLOW + "impROS" + ANSI_RESET);
System.out.println(ANSI_BLUE + "impROS" + ANSI_RESET);
System.out.println(ANSI_PURPLE + "impROS" + ANSI_RESET);
System.out.println(ANSI_CYAN + "impROS" + ANSI_RESET);
System.out.println(ANSI_WHITE + "impROS" + ANSI_RESET);
    }

}
