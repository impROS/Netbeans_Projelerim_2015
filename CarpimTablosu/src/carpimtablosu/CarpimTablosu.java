
package carpimtablosu;

public class CarpimTablosu {

    public static void main(String[] args) 
    {
        int sonuc=1;
    for(int i=1;i<=10;i++)
    {
        for(int j=1;j<=10;j++)
                {
                    sonuc=i*j;
                System.out.print(i+" * "+j+" = "+sonuc+"\t");
                }
         System.out.println("");
     }
    }
    
}
